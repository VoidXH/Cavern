namespace Cavern.Format {
    /// <summary>
    /// Audio bit depth choices.
    /// </summary>
    public enum BitDepth {
        /// <summary>
        /// 8-bit integer.
        /// </summary>
        Int8 = 8,
        /// <summary>
        /// 16-bit integer.
        /// </summary>
        Int16 = 16,
        /// <summary>
        /// 24-bit integer.
        /// </summary>
        Int24 = 24,
        /// <summary>
        /// 32-bit floating point.
        /// </summary>
        Float32 = 32,
    }
}
